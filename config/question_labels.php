function get_formatted_answer($key, $value) {
    global $learning_options;
    
    $prefix = explode('_', $key)[0];
    
    if ($prefix === 'learning') {
        if (isset($learning_options[$key][$value])) {
            return $learning_options[$key][$value] . " ($value)";
        }
    } elseif ($prefix === 'erq') {
        $labels = [1 => 'Strongly Disagree', 2 => 'Disagree', 3 => 'Somewhat Disagree', 4 => 'Neutral', 5 => 'Somewhat Agree', 6 => 'Agree', 7 => 'Strongly Agree'];
        return "$value (" . ($labels[$value] ?? 'Unknown') . ")";
    } elseif ($prefix === 'cat') {
        $labels = [1 => 'Not at all', 2 => 'Very little', 3 => 'A little', 4 => 'Somewhat', 5 => 'Moderately', 6 => 'Quite a bit', 7 => 'A great deal'];
        return "$value (" . ($labels[$value] ?? 'Unknown') . ")";
    } elseif ($prefix === 'dass') {
        $labels = [
            0 => 'Did not apply to me at all',
            1 => 'Applied to me to some degree, or some of the time',
            2 => 'Applied to me to a considerable degree or a good part of time',
            3 => 'Applied to me very much or most of the time'
        ];
        return "$value (" . ($labels[$value] ?? 'Unknown') . ")";
    } elseif ($prefix === 'ars') {
        $labels = [1 => 'Strongly Agree', 2 => 'Agree', 3 => 'Neutral', 4 => 'Disagree', 5 => 'Strongly Disagree'];
        return "$value (" . ($labels[$value] ?? 'Unknown') . ")";
    } elseif ($prefix === 'ffmq') {
        $labels = [1 => 'Never or very rarely true', 2 => 'Rarely true', 3 => 'Sometimes true', 4 => 'Often true', 5 => 'Very often or always true'];
        return "$value (" . ($labels[$value] ?? 'Unknown') . ")";
    }
    
    return $value;
}
